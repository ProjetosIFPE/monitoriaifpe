DROP SCHEMA IF EXISTS PROJETO_PERIODO;
CREATE SCHEMA PROJETO_PERIODO;

DROP TABLE IF EXISTS USUARIO;
CREATE TABLE USUARIO (
	USUARIO_NOME VARCHAR(200)
	USUARIO_LOGIN VARCHAR(200),
	USUARIO_SENHA VARCHAR(200),
	PRIMARY KEY(USUARIO_LOGIN)
);

DROP TABLE IF EXISTS DEPARTAMENTO;
CREATE TABLE DEPARTAMENTO ( 
	DEPARTAMENTO_DS VARCHAR(50),
	PRIMARY KEY(DEPARTAMENTO_DS)
);

DROP TABLE IF EXISTS FUNCIONARIO;
CREATE TABLE FUNCIONARIO ( 
	USUARIO_CD INT,
	FUNCIONARIO_SIAPE VARCHAR(20),
	DEPARTAMENTO_DS VARCHAR(50)
	PRIMARY KEY(FUNCIONARIO_SIAPE)
);

DROP TABLE IF EXISTS CURSO;
CREATE TABLE CURSO (
	CURSO_CD INT,
	GRAU ENUM('SUPERIOR', 'TECNICO'),	
	CURSO_DS VARCHAR(20),
	PRIMARY KEY(CURSO_CD)
);

DROP TABLE IF EXISTS DISCIPLINA;
CREATE TABLE DISCIPLINA ( 
	DISCIPLINA_CD INT,
	DISCIPLINA_DS varchar(20),
	CURSO_CD INT,
	PRIMARY KEY(DISCIPLINA_CD),
	FOREIGN KEY (CURSO_CD) REFERENCES CURSO(CURSO_CD)
);

DROP TABLE IF EXISTS DISCIPLINA_ALUNO;
CREATE TABLE DISCIPLINA_ALUNO ( 
	ALUNO_CD int,
	DISCIPLINA_CD int
	PRIMARY KEY (ALUNO_CD, DISCIPLINA_CD)
);

DROP TABLE IF EXISTS ALUNO;
CREATE TABLE ALUNO (
	ALUNO_CD int,
	ALUNO_MATRICULA varchar(20),
	ALUNO_DS varchar(20),
	CURSO_CD int,
	PRIMARY KEY (ALUNO_CD)
	
);

DROP TABLE IF EXISTS MONITOR;
CREATE TABLE MONITOR ( 
	MONITOR_CD int,
	ALUNO_CD int,
	MODALIDADE ENUM('MONITOR', 'VOLUNTARIO'),
	DISCIPLINA_CD int
	PRIMARY KEY(MONITOR_CD),
	FOREIGN KEY(ALUNO_CD) REFERENCES ALUNO(ALUNO_CD)
	FOREIGN KEY(DISCIPLINA_CD) REFERENCES DISCIPLINA()
);


DROP TABLE IF EXISTS ATIVIDADE;
CREATE TABLE ATIVIDADE (
	ATIVIDADE_CD int,
	ATIVIDADE_DATA date,
	HORARIO_ENTRADA timestamp,
	HORARIO_ENTRADA timestamp
	SEMANA_CD int,
	PRIMARY KEY(ATIVIDADE_CD)
	FOREIGN KEY (SEMANA_CD) REFERENCES SEMANA(SEMANA_CD)
);



DROP TABLE IF EXISTS RELATORIO_FREQUENCIA;
CREATE TABLE RELATORIO_FREQUENCIA (
	RELATORIO_CD int,
	RELATORIO_ANO int(4, 0),
	RELATORIO_MES int(2, 0),
	RELATORIO_CARGA_HORARIA int,
	MONITOR_CD int,
	RELATORIO_EDITAL varchar(6),
	ORIENTADOR_CD int,
	FUNCIONARIO_CD int,
	PRIMARY KEY
)

